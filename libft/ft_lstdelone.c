#include "libft.h"

void	ft_lstdelone(t_list **alst, void (*del)(void *, size_t))
{
	if (*alst)
	{
		if ((*alst)->obj)
			(*del)((*alst)->obj, (*alst)->dim);
		ft_memdel((void**)alst);
	}
}
